  // 대학교별 학과 목록 가져오기
  static List<String> getMajorsByUniversity(String universityName) {
    return _universityMajors[universityName] ?? [];
  }

  // 학과 검색
  static List<String> searchMajors(String universityName, String query) {
    final majors = getMajorsByUniversity(universityName);
    if (query.isEmpty) return majors;
    
    final lowercaseQuery = query.toLowerCase();
    return majors.where((major) => 
      major.toLowerCase().contains(lowercaseQuery)
    ).toList();
  }

  // 모든 대학교 목록
  static List<String> getAllUniversities() {
    return _universityMajors.keys.toList();
  }

  // 대학교 검색
  static List<String> searchUniversities(String query) {
    if (query.isEmpty) return getAllUniversities();
    
    final lowercaseQuery = query.toLowerCase();
    return _universityMajors.keys.where((university) => 
      university.toLowerCase().contains(lowercaseQuery)
    ).toList();
  }
